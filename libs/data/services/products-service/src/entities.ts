export interface IProduct {

    /**
     * Name of product
     *
     * @type {string}
     * @memberof IProduct
     */
    name: string;


    /**
     * Price of product
     *
     * @type {number}
     * @memberof IProduct
     */
    price: number;


    /**
     * SKU of product
     *
     * @type {string}
     * @memberof IProduct
     */
    sku: string;
}